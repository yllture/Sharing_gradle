package com.neu.Sharing.entity;

public class Path {
	private String hash;
	private String gbFile;
	private String apFileS;
	private String apFileP;
	private String encFile;
	public String getHash() {
		return hash;
	}
	public void setHash(String hash) {
		this.hash = hash;
	}
	public String getGbFile() {
		return gbFile;
	}
	public void setGbFile(String gbFile) {
		this.gbFile = gbFile;
	}
	public String getApFileS() {
		return apFileS;
	}
	public void setApFileS(String apFileS) {
		this.apFileS = apFileS;
	}
	public String getApFileP() {
		return apFileP;
	}
	public void setApFileP(String apFileP) {
		this.apFileP = apFileP;
	}
	public String getEncFile() {
		return encFile;
	}
	public void setEncFile(String encFile) {
		this.encFile = encFile;
	}
	
	

}
