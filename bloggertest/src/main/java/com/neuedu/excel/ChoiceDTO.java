package com.neuedu.excel;

import com.alibaba.excel.annotation.ExcelProperty;
import com.alibaba.excel.metadata.BaseRowModel;

public class ChoiceDTO extends BaseRowModel{

	@ExcelProperty(index = 0 , value = "题型")
	 private String type;
	 
	 @ExcelProperty(index = 1 , value = "题目")
	 private String title;

	 @ExcelProperty(index = 2, value = "选项A")
	 private String optionA;
	 
	 @ExcelProperty(index = 3, value = "选项B")
	 private String optionB;
	 
	 @ExcelProperty(index = 4, value = "选项C")
	 private String optionC;
	 
	 @ExcelProperty(index = 5, value = "选项D")
	 private String optionD;
	 
	 @ExcelProperty(index = 6, value = "选项E")
	 private String optionE;
	 
	 @ExcelProperty(index = 7, value = "正确选项")
	 private String optionture;
	 
	 @ExcelProperty(index = 8, value = "知识点")
	 private String knowledge;
	 
	 @ExcelProperty(index = 9, value = "难度")
	 private String difficulity;
	 
	 @ExcelProperty(index = 10, value = "解析")
	 private String analysis;
	 
	 public ChoiceDTO() {
		 super();
	 }
	 
	 public ChoiceDTO(String type,String title,String optionA,String optionB, String optionC,
			 String optionD, String optionE,String optionture,String knowledge,
			 String difficulity,String analysis) {
		 super();
		 this.type = type;
		 this.title = title;
		 this.optionA = optionA;
		 this.optionB = optionB;
		 this.optionC = optionC;
		 this.optionD = optionD;
		 this.optionE = optionE;
		 this.optionture = optionture;
		 this.knowledge = knowledge;
		 this.difficulity = difficulity;
		 this.analysis = analysis;
		 
		 
	 }
	 
		
	 public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getOptionA() {
		return optionA;
	}

	public void setOptionA(String optionA) {
		this.optionA = optionA;
	}

	public String getOptionB() {
		return optionB;
	}

	public void setOptionB(String optionB) {
		this.optionB = optionB;
	}

	public String getOptionC() {
		return optionC;
	}

	public void setOptionC(String optionC) {
		this.optionC = optionC;
	}

	public String getOptionD() {
		return optionD;
	}

	public void setOptionD(String optionD) {
		this.optionD = optionD;
	}

	public String getOptionE() {
		return optionE;
	}

	public void setOptionE(String optionE) {
		this.optionE = optionE;
	}

	public String getOptionture() {
		return optionture;
	}

	public void setOptionture(String optionture) {
		this.optionture = optionture;
	}

	public String getKnowledge() {
		return knowledge;
	}

	public void setKnowledge(String knowledge) {
		this.knowledge = knowledge;
	}

	public String getDifficulity() {
		return difficulity;
	}

	public void setDifficulity(String difficulity) {
		this.difficulity = difficulity;
	}

	public String getAnalysis() {
		return analysis;
	}

	public void setAnalysis(String analysis) {
		this.analysis = analysis;
	}

}
