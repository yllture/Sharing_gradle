package com.neuedu.excel;

import com.alibaba.excel.annotation.ExcelProperty;
import com.alibaba.excel.metadata.BaseRowModel;

public class FillDTO  extends BaseRowModel{
	 @ExcelProperty(index = 0 , value = "题型")
	 private String type;
	 
	 @ExcelProperty(index = 1 , value = "题目")
	 private String title;
	 
	 @ExcelProperty(index = 2 , value = "参考答案1")
	 private String answer1;
	
	 @ExcelProperty(index = 3 , value = "参考答案2")
	 private String answer2;
	 
	 @ExcelProperty(index = 4 , value = "参考答案3")
	 private String answer3;

	 @ExcelProperty(index = 5 , value = "知识点")
	 private String knowledge;
	 
	 @ExcelProperty(index = 6 , value = "难度")
	 private String difficulty;
	 
	 @ExcelProperty(index = 7 , value = "解析")
	 private String analysis;
	 
	 public FillDTO() {
		 super();
	 }
	 
	 public FillDTO(String type,String title,String answer1,String answer2,String answer3,
			 String knowledge,String difficulty,String analysis) {
		 super();
		 this.type = type;
		 this.title = title;
		 this.answer1 = answer1;
		 this.answer2 = answer2;
		 this.answer3 = answer3;
		 this.knowledge = knowledge;
		 this.difficulty = difficulty;
		 this.analysis = analysis;
		 
	 }

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAnswer1() {
		return answer1;
	}

	public void setAnswer1(String answer1) {
		this.answer1 = answer1;
	}

	public String getAnswer2() {
		return answer2;
	}

	public void setAnswer2(String answer2) {
		this.answer2 = answer2;
	}

	public String getAnswer3() {
		return answer3;
	}

	public void setAnswer3(String answer3) {
		this.answer3 = answer3;
	}

	public String getKnowledge() {
		return knowledge;
	}

	public void setKnowledge(String knowledge) {
		this.knowledge = knowledge;
	}

	public String getDifficulty() {
		return difficulty;
	}

	public void setDifficulty(String difficulty) {
		this.difficulty = difficulty;
	}

	public String getAnalysis() {
		return analysis;
	}

	public void setAnalysis(String analysis) {
		this.analysis = analysis;
	}
	 
}
